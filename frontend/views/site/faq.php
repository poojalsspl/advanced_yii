            <div class="box-body">
            Following are the list of fixed data points are found in every case laws delivered by the court of law.<br />
1.  Name  or Names of Judges<br />
2.  Advocated appearing for the appellant<br />
3.  Advocated appearing for the respondent<br />
4.  Court Name<br />
5.  Judgment Date<br />
6.  Appellant Name<br />
7.  Respondent Name<br />
8.  Journal Citation<br />
9.  Appeal Number / Case number<br />
10. Case Law Disposition<br />
11. Bench Type<br />
12. Jurisdiction<br />
13. Various acts and sections imposed in case laws<br />
14. Judgments referred or cited n the judgments<br />
15. Various Other Judgments Cited in the existing case law<br />
16. Judgment Category ( based on the various acts and section imposed)<br /><br />
These are the fixed data point that exists in most of the judgments. At times the journal citation may not be found if the case law is not published in and of the officially endorsed law publication like AIR, SCC, etc
 
